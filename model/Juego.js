class Juego{
    nombre;
    precio;
    empresa;
    genero;
    imagen;
    descripcion;

    constructor(nombre, precio, empresa, genero, imagen, descripcion){
        this.nombre = nombre;
        this.precio = precio;
        this.empresa = empresa;
        this.genero = genero;
        this.imagen = imagen;
        this.descripcion = descripcion;
    }
}